///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

var Options = function () {},
        canvasImage;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

Options.prototype = 
{
    // ===========================================================================================================================
    // PRELOAD
    // --------------------------------------------------------------------------------------------------------------------------
    // Pre-loads the option menu's assets
    // ===========================================================================================================================
    preload: function() 
    {
        // Load canvas image
        game.load.image('canvas-image', this.canvasImage.src, this.canvasImage);
    },
    
    // ===========================================================================================================================
    // CREATE
    // --------------------------------------------------------------------------------------------------------------------------
    // Create 
    // ===========================================================================================================================
    create: function() 
    {
        // Add background image
        game.add.sprite(0, 0, 'canvas-image');
    }
};

