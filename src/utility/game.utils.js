var gameUtils = 
{
    // ===========================================================================================================================
    // CENTER GAME OBJECTS
    // --------------------------------------------------------------------------------------------------------------------------
    // Centers passed in game objects by setting anchor accordingly
    // 
    // @param {array} objects
    //  Array of phaser game objects
    // ===========================================================================================================================
    centerGameObjects : function(objects)
    {
        objects.forEach(function (object) 
        {
           object.anchor.setTo(0.5);
        });
    }
};

